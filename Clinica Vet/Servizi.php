        </ul>    
        <em>Sì, è obbligatorio per legge dotare un cane di microchip.</em>Il microchip risulta indispensabile in molti casi, come quando l'animale deve essere per esempio identificato, vaccinato o ceduto.
        Esso consiste in una <span lang="en">trasponder</span>, che si presenta sotto forma di una capsula di vetro biocompatibile; tale dispositivo funziona come una radio antenna
        pronta a ricevere il segnale del lettore di microchip, grazie al quale si ottiene un codice di 15 cifre attraverso il quale è possibile risalire al proprietario del cane.
        La sua appliczione può essere effettuata solo da un veterinario abilitato in ambulatorio, con una iniezione sottocutanea assolutamente innocua per l'animale.
